package com.punchingwood.minechat.permissions;

/*****************************************************************************
 * Represents the default possible values for a given permission
 *
 *****************************************************************************/
public enum PluginPermissionDefault
{
    /** Default value is true for all players */
    TRUE,
    
    /** Default value is false for all players*/
    FALSE,
    
    /** Default value is true if player is Op */
    OP
    ;
}
